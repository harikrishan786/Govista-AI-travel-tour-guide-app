  // Helper Methods
  static List<Map<String, dynamic>> getByRegion(String region) {
    return allDestinations
        .where((d) => d['region'] == region.toLowerCase())
        .toList();
  }

  static List<Map<String, dynamic>> getPopular({int limit = 10}) {
    var sorted = List<Map<String, dynamic>>.from(allDestinations);
    sorted.sort(
      (a, b) =>
          (b['popularityScore'] ?? 0).compareTo(a['popularityScore'] ?? 0),
    );
    return sorted.take(limit).toList();
  }

  static List<Map<String, dynamic>> search(String query) {
    String q = query.toLowerCase();
    return allDestinations
        .where(
          (d) =>
              d['name'].toString().toLowerCase().contains(q) ||
              d['state'].toString().toLowerCase().contains(q),
        )
        .toList();
  }
